// Guards a value read off the backend (`User.language`, a plain nullable
// string column — see shared_models.models.User) before handing it to
// `useSettingsStore().setLanguage`, which expects a real `Language`.
export function isSupportedLanguage(value: string | null | undefined): value is Language {
  return !!value && LANGUAGES.some((option) => option.value === value);
}

